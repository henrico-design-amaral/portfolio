const LANG_STORAGE_KEY = 'henrico_lang';
let currentLang = 'pt';

function setLanguage(lang) {
  const safeLang = lang === 'en' ? 'en' : 'pt';
  currentLang = safeLang;
  document.documentElement.lang = safeLang === 'pt' ? 'pt-BR' : 'en';
  document.documentElement.dataset.lang = safeLang;
  localStorage.setItem(LANG_STORAGE_KEY, safeLang);

  const dict = COPY[safeLang] || COPY.pt;
  document.querySelectorAll('[data-t]').forEach(el => {
    const key = el.getAttribute('data-t');
    if (typeof dict[key] !== 'string') return;
    const val = dict[key];
    if (val.includes('{em}')) {
      el.innerHTML = val.replace('{em}', '<em class="text-accent-500 font-normal">').replace('{/em}', '</em>');
    } else {
      el.innerHTML = val;
    }
  });
  document.querySelectorAll('[data-t-alt]').forEach(el => {
    const key = el.getAttribute('data-t-alt');
    if (dict[key]) el.setAttribute('alt', dict[key]);
  });
  document.querySelectorAll('[data-t-aria-label]').forEach(el => {
    const key = el.getAttribute('data-t-aria-label');
    if (dict[key]) el.setAttribute('aria-label', dict[key]);
  });
  document.querySelectorAll('[data-t-title]').forEach(el => {
    const key = el.getAttribute('data-t-title');
    if (dict[key]) el.setAttribute('title', dict[key]);
  });

  document.querySelectorAll('[data-lang-toggle]').forEach(toggle => {
    toggle.dataset.currentLang = safeLang;
    toggle.setAttribute('aria-label', safeLang === 'pt' ? 'Selecionar idioma' : 'Select language');
  });

  document.querySelectorAll('.lang-toggle__option').forEach(btn => {
    const active = btn.dataset.lang === safeLang;
    btn.setAttribute('aria-pressed', String(active));
    btn.classList.toggle('active', active);
  });

  const announcer = document.getElementById('sr-announcer');
  if (announcer) {
    announcer.textContent = safeLang === 'pt' ? 'Idioma alterado para Português' : 'Language changed to English';
  }

  const mobileMenuToggle = document.getElementById('mobile-menu-toggle');
  if (mobileMenuToggle) {
    const isOpen = mobileMenuToggle.getAttribute('aria-expanded') === 'true';
    mobileMenuToggle.setAttribute('aria-label', safeLang === 'pt'
      ? (isOpen ? 'Fechar menu' : 'Abrir menu')
      : (isOpen ? 'Close menu' : 'Open menu'));
  }

}

function initLanguageToggle() {
  const saved = localStorage.getItem(LANG_STORAGE_KEY);
  const initial = saved === 'en' || saved === 'pt' ? saved : 'pt';

  document.querySelectorAll('.lang-toggle__option').forEach(btn => {
    btn.addEventListener('click', () => setLanguage(btn.dataset.lang));
  });

  setLanguage(initial);
}

const mobileMenuToggle = document.getElementById('mobile-menu-toggle');
const mobileMenu = document.getElementById('mobile-menu');
if (mobileMenuToggle && mobileMenu) {
  mobileMenuToggle.addEventListener('click', () => {
    const isOpen = mobileMenuToggle.getAttribute('aria-expanded') === 'true';
    mobileMenu.hidden = isOpen;
    mobileMenuToggle.setAttribute('aria-expanded', isOpen ? 'false' : 'true');
    mobileMenuToggle.setAttribute('aria-label', currentLang === 'pt'
      ? (isOpen ? 'Abrir menu' : 'Fechar menu')
      : (isOpen ? 'Open menu' : 'Close menu'));
  });

  mobileMenu.querySelectorAll('a').forEach(link => {
    link.addEventListener('click', () => {
      mobileMenu.hidden = true;
      mobileMenuToggle.setAttribute('aria-expanded', 'false');
      mobileMenuToggle.setAttribute('aria-label', currentLang === 'pt' ? 'Abrir menu' : 'Open menu');
    });
  });
  document.addEventListener('keydown', event => {
    if (event.key !== 'Escape') return;
    if (mobileMenuToggle.getAttribute('aria-expanded') !== 'true') return;

    mobileMenu.hidden = true;
    mobileMenuToggle.setAttribute('aria-expanded', 'false');
    mobileMenuToggle.setAttribute('aria-label', currentLang === 'pt' ? 'Abrir menu' : 'Open menu');
    mobileMenuToggle.focus();
  });
}
/* ── Nav Scroll behavior ── */
let navTicking = false;
window.addEventListener('scroll', () => {
  if (navTicking) return;
  navTicking = true;
  window.requestAnimationFrame(() => {
    const nav = document.getElementById('nav');
    if (nav) {
      const currentScroll = window.pageYOffset || document.documentElement.scrollTop;
      if (currentScroll > 60) {
        if (!nav.classList.contains('nav-scrolled')) {
          nav.classList.add('shadow-sm', 'nav-scrolled');
        }
      } else {
        if (nav.classList.contains('nav-scrolled')) {
          nav.classList.remove('shadow-sm', 'nav-scrolled');
        }
      }
    }
    navTicking = false;
  });
}, { passive: true });

function initCaseMosaics() {
  const slots = ['a', 'b', 'c', 'd', 'e', 'f'];

  document.querySelectorAll('.case-reframe__plate-images').forEach(gallery => {
    const frames = Array.from(gallery.querySelectorAll(':scope > .case-reframe__plate-image'));
    if (!frames.length) return;

    gallery.dataset.count = String(Math.min(frames.length, slots.length));

    frames.forEach((frame, index) => {
      const image = frame.querySelector('img');
      const width = Number(image?.getAttribute('width')) || 0;
      const height = Number(image?.getAttribute('height')) || 0;

      frame.dataset.slot = slots[index] || 'extra';
      if (!width || !height) return;

      const ratio = width / height;
      frame.style.setProperty('--media-ratio', `${width} / ${height}`);
      frame.dataset.format = ratio >= 2.4
        ? 'ultrawide'
        : ratio >= 1.4
          ? 'wide'
          : ratio >= 1
            ? 'standard'
            : ratio >= 0.62
              ? 'portrait'
              : 'tall';
    });
  });
}

function initCaseLightbox() {
  const images = Array.from(document.querySelectorAll('.case-reframe__plate-image img'));
  if (!images.length) return;

  const lightbox = document.createElement('div');
  lightbox.className = 'case-lightbox';
  lightbox.hidden = true;
  lightbox.setAttribute('role', 'dialog');
  lightbox.setAttribute('aria-modal', 'true');
  lightbox.setAttribute('aria-label', 'Imagem ampliada do case / Enlarged case image');
  lightbox.innerHTML = `
    <div class="case-lightbox__backdrop" data-lightbox-close></div>
    <figure class="case-lightbox__panel">
      <button class="case-lightbox__close" type="button" data-lightbox-close></button>
      <img class="case-lightbox__image" alt="">
      <figcaption class="case-lightbox__caption"></figcaption>
    </figure>
  `;
  document.body.appendChild(lightbox);

  const lightboxImage = lightbox.querySelector('.case-lightbox__image');
  const lightboxCaption = lightbox.querySelector('.case-lightbox__caption');
  const closeButton = lightbox.querySelector('.case-lightbox__close');
  let activeTrigger = null;
  let previousOverflow = '';

  const labels = () => {
    const isEnglish = currentLang === 'en' || document.documentElement.dataset.lang === 'en';
    return {
      close: isEnglish ? 'Close' : 'Fechar',
      zoom: isEnglish ? 'Zoom' : 'Zoom',
      enlarge: isEnglish ? 'Open enlarged image' : 'Ampliar imagem'
    };
  };

  const syncLabels = () => {
    const copy = labels();
    closeButton.textContent = copy.close;
    document.querySelectorAll('.case-lightbox-trigger').forEach(trigger => {
      const image = trigger.querySelector('img');
      trigger.setAttribute('aria-label', `${copy.enlarge}: ${image?.alt || 'case'}`);
      const hint = trigger.querySelector('.case-lightbox-hint');
      if (hint) hint.textContent = copy.zoom;
    });
  };

  const closeLightbox = () => {
    if (lightbox.hidden) return;

    lightbox.hidden = true;
    document.body.classList.remove('case-lightbox-open');
    document.body.style.overflow = previousOverflow;
    lightboxImage.removeAttribute('src');

    if (activeTrigger) activeTrigger.focus({ preventScroll: true });
    activeTrigger = null;
  };

  const openLightbox = (trigger, image) => {
    activeTrigger = trigger;
    previousOverflow = document.body.style.overflow;
    lightboxImage.src = image.currentSrc || image.src;
    lightboxImage.alt = image.alt || '';
    lightboxCaption.textContent = image.alt || '';
    lightbox.hidden = false;
    document.body.classList.add('case-lightbox-open');
    document.body.style.overflow = 'hidden';
    syncLabels();
    closeButton.focus({ preventScroll: true });
  };

  images.forEach(image => {
    if (image.closest('.case-lightbox-trigger')) return;

    const trigger = document.createElement('button');
    trigger.type = 'button';
    trigger.className = 'case-lightbox-trigger';

    const hint = document.createElement('span');
    hint.className = 'case-lightbox-hint';

    image.parentNode.insertBefore(trigger, image);
    trigger.appendChild(image);
    trigger.appendChild(hint);
    trigger.addEventListener('click', () => openLightbox(trigger, image));
  });

  lightbox.querySelectorAll('[data-lightbox-close]').forEach(control => {
    control.addEventListener('click', closeLightbox);
  });

  document.addEventListener('keydown', event => {
    if (event.key === 'Escape') closeLightbox();
  });

  syncLabels();
}

