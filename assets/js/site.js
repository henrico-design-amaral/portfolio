function reveal(selector, vars = {}) {
  gsap.utils.toArray(selector).forEach(el => {
    gsap.from(el, {
      scrollTrigger: { 
        trigger: el, 
        start: 'top 85%',
        toggleActions: 'play none none none'
      },
      opacity: 0,
      y: 40,
      scale: 0.98,
      filter: 'blur(10px)',
      duration: 1.6,
      ease: 'power4.out',
      clearProps: 'filter,opacity,transform',
      ...vars
    });
  });
}

if (!motionReduced) {
  reveal('.section-heading', { duration: 1.8, filter: 'blur(10px)', y: 60 });
  reveal('.terr-item', { stagger: 0.15 });
  reveal('.reveal-step', { stagger: 0.2, y: 50, scale: 0.95 });
  reveal('.case-row', { stagger: 0.2, y: 60 });
  reveal('.impact-cell', { stagger: 0.1, x: -30, y: 0 });

  // Parallax elements
  gsap.utils.toArray('[data-parallax]').forEach(el => {
    const speed = parseFloat(el.getAttribute('data-parallax')) || 0.1;
    gsap.to(el, {
      y: () => -(ScrollTrigger.maxScroll(window) * speed),
      ease: 'none',
      scrollTrigger: {
        trigger: el.closest('section') || 'body',
        start: 'top bottom',
        end: 'bottom top',
        scrub: 1
      }
    });
  });

  ScrollTrigger.create({
    trigger: '#impact',
    start: 'top 75%',
    once: true,
    onEnter: () => {
      document.querySelectorAll('.ic').forEach(el => {
        const to = parseInt(el.dataset.to, 10);
        const fmt = el.dataset.fmt || '';
        const obj = { v: 0 };
        gsap.to(obj, {
          v: to,
          duration: 2,
          ease: 'power3.out',
          onUpdate: () => {
            if (fmt === 'K') {
              el.textContent = obj.v < 1000 ? '<1K' : Math.round(obj.v / 1000) + 'K';
            } else {
              el.textContent = Math.round(obj.v);
            }
          }
        });
      });
    }
  });

  // Parallax on Case Images
  gsap.utils.toArray('.case-card').forEach(card => {
    const imgContainer = card.querySelector('.aspect-\\[4\\/3\\]');
    if (imgContainer) {
      gsap.to(imgContainer, {
        scrollTrigger: {
          trigger: card,
          start: 'top bottom',
          end: 'bottom top',
          scrub: true
        },
        y: -30,
        ease: 'none'
      });
    }
  });

  // Scrub Text
  const scrubTarget = document.querySelector('.scrub-text');
  if (scrubTarget) {
    const words = scrubTarget.innerText.split(' ');
    scrubTarget.innerHTML = '';
    words.forEach(w => {
      const span = document.createElement('span');
      span.textContent = w + ' ';
      span.style.opacity = '0.2';
      span.style.transition = 'opacity 0.2s cubic-bezier(0.4, 0, 0.2, 1)';
      scrubTarget.appendChild(span);
    });
    
    gsap.to(scrubTarget.children, {
      scrollTrigger: {
        trigger: scrubTarget,
        start: 'top 80%',
        end: 'top 40%',
        scrub: 0.5
      },
      opacity: 1,
      stagger: 0.1,
      ease: 'none'
    });
  }
}

/* ── Vanilla JS Infinite Grid & Flashlight ── */
const gridBg = document.getElementById('vanilla-hero-bg');
if (gridBg && !motionReduced) {
  let mouseX = window.innerWidth / 2;
  let mouseY = window.innerHeight / 2;
  let gridOffsetX = 0;
  let gridOffsetY = 0;
  
  window.addEventListener('mousemove', (e) => {
    const rect = gridBg.getBoundingClientRect();
    mouseX = e.clientX - rect.left;
    mouseY = e.clientY - rect.top;
    gridBg.style.setProperty('--mouse-x', `${mouseX}px`);
    gridBg.style.setProperty('--mouse-y', `${mouseY}px`);
  });

  function animateGrid() {
    gridOffsetX = (gridOffsetX + 0.5) % 40;
    gridOffsetY = (gridOffsetY + 0.5) % 40;
    const patterns = gridBg.querySelectorAll('.grid-pattern');
    patterns.forEach(p => {
      p.style.backgroundPosition = `${gridOffsetX}px ${gridOffsetY}px`;
    });
    requestAnimationFrame(animateGrid);
  }
  animateGrid();
}

